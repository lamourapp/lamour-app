/**
 * Українська множина/узгодження для UI-рядків.
 *
 * Чому це тут, а не inline: specialistTerm налаштовується користувачем
 * («Майстер» / «Спеціаліст» / «Барбер»), а склейка `${term}и` дає
 * «Майстери» замість «Майстри». Морфологія — в одному місці.
 */

/**
 * Plural form для назви ролі (Н.в., мн.): «Майстер» → «Майстри», «Спеціаліст»
 * → «Спеціалісти», «Барбер» → «Барбери». Працює евристично; для незнайомих
 * форм додає просто «и».
 *
 * @example
 * pluralizeTerm("Майстер")    // "Майстри"
 * pluralizeTerm("Спеціаліст") // "Спеціалісти"
 */
export function pluralizeTerm(term: string): string {
  const t = term.trim();
  if (!t) return t;
  // Викидне «е» перед плавним «р»: Майстер → Майстри, Перукар → Перукарі
  // (для -кар використовуємо -і, бо «Перукари» звучить, але «Перукарі» — літературна).
  if (/тер$/i.test(t)) return t.slice(0, -2) + "ри";
  if (/кар$/i.test(t)) return t + "і";
  // -ліст, -лог, -ник, -бер → +и (Спеціалісти, Барбери).
  return t + "и";
}

/**
 * Узгодження іменника з числівником (українська трьохформна модель).
 * 1 товар, 2-4 товари, 5+ товарів (з урахуванням 11-14 як «багато»).
 *
 * @example
 * pluralizeCount(1, ["товар", "товари", "товарів"])  // "товар"
 * pluralizeCount(2, ["товар", "товари", "товарів"])  // "товари"
 * pluralizeCount(5, ["товар", "товари", "товарів"])  // "товарів"
 * pluralizeCount(21, ["товар", "товари", "товарів"]) // "товар"
 */
export function pluralizeCount(n: number, forms: [string, string, string]): string {
  const abs = Math.abs(Math.trunc(n));
  const mod100 = abs % 100;
  const mod10 = abs % 10;
  if (mod100 >= 11 && mod100 <= 14) return forms[2];
  if (mod10 === 1) return forms[0];
  if (mod10 >= 2 && mod10 <= 4) return forms[1];
  return forms[2];
}
